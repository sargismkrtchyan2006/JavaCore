package classwork.calculator;

public class Calculator {
    double plus(double x, double y) {
        return (x + y);
    }

    double minus(double x, double y) {
        return (x - y);
    }

    double multiple(double x, double y) {
        return (x * y);
    }

    double divide(double x, double y) {
        return (x / y);
    }
}
